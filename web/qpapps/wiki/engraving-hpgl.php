<h1>Postup pre vytvorenie súboru s vektorovou grafikou vhodnou pre import do QuickPanel-u<br>(v programe CorelDraw X4)
</h1>
<ol>

    <li>
        Po vytvorení grafiky (hlavne ak boli použité texty) je potrebné konvertovať tieto na krivky.<br>
        <img src="img/engraving-hpgl-01.png" alt="engraving-hpgl-01.png, 16kB" title="engraving-hpgl-01" height="385"
             width="624">
    </li>

    <li>
        Nastavíme farbu výplne na "žiadnu" a farbu obrysu na čiernu.<br>
        <img src="img/engraving-hpgl-02.png" alt="engraving-hpgl-02.png, 27kB" title="engraving-hpgl-02" height="302"
             width="536">
    </li>

    <li>
        Zvolíme nástroj "Kontura"<br>
        <img src="img/engraving-hpgl-03.png" alt="engraving-hpgl-03.png, 39kB" title="engraving-hpgl-03" height="308"
             width="648">
    </li>

    <li>
        Zvolíme kontúru "do vnútra".<br>
        Predpokladajme, že chceme použiť gravírovací nástroj s priemerom 0,4mm. Nastavíme teda vzdialenosť jednotlivých
        kontúr na polomer nástroja: 0,2mm.<br>
        A v políčku počet kontúr nastavíme maximálny možný počet.<br>
        <img src="img/engraving-hpgl-04.png" alt="engraving-hpgl-04.png, 86kB" title="engraving-hpgl-04" height="357"
             width="563">
    </li>

    <li>
        Následne rozdelíme skupinu kontúr na jednotlivé kontúry.<br>
        (toto menu sa nám zobrazí, len ak klikneme pravým tlačítkom na niektorú z vnútorných kontúr, nie obrysovú!)<br>
        <img src="img/engraving-hpgl-05.png" alt="engraving-hpgl-05.png, 78kB" title="engraving-hpgl-05" height="383"
             width="533">
    </li>

    <li>
        Ešte musíme zrušiť všetko zoskupenie objektov.<br>
        <img src="img/engraving-hpgl-06.png" alt="engraving-hpgl-06.png, 80kB" title="engraving-hpgl-06" height="301"
             width="520">
    </li>

    <li>
        Keďža vonkajšia kontúra je pre nás hranicou gravírovania, nechceme aby po tejto kontúre jazdil nástroj
        (rozšírilo by nám to gravírovanú grafiku)<br>
        Preto vonkajšiu kontúru vymažeme.<br>
        <img src="img/engraving-hpgl-07.png" alt="engraving-hpgl-07.png, 78kB" title="engraving-hpgl-07" height="295"
             width="507">
    </li>

    <li>
        Hotovo, toto je výsledok, ktorý môžeme uložiť do súboru:<br>
        <img src="img/engraving-hpgl-08.png" alt="engraving-hpgl-08.png, 60kB" title="engraving-hpgl-08" height="275"
             width="499">
    </li>

    <li>
        Z menu Súbor zvolíme v Coreli možnosť Exportovať a zvolíme formát súboru "PLT".<br>
        V nastaveniach formátu súboru stačí nastaviť rozlíšenie krivky na 0. To znamená maximálne rozlíšenie.<br>
        <img src="img/engraving-hpgl-09.png" alt="engraving-hpgl-09.png, 75kB" title="engraving-hpgl-09" height="426"
             width="603">
    </li>

    <li>
        Súbor je vytvorený, prepneme sa do QuickPanel-u a naimportujeme daný súbor. Nezabudnime nastaviť správny priemer
        gravírovacieho nástroja.<br>
        <img src="img/engraving-hpgl-10.png" alt="engraving-hpgl-10.png, 42kB" title="engraving-hpgl-10" height="520"
             width="311">
    </li>

    <li>
        Hotovo.<br>
        <img src="img/engraving-hpgl-11.png" alt="engraving-hpgl-11.png, 23kB" title="engraving-hpgl-11" height="426"
             width="475">
    </li>

</ol>
